<?php
// concatenation
// strings are joined together with a "."
echo "string one" . " " . "string two\n";
// the backslash is an escape character. eg, \n is new line
// it can be used to include special chars
echo "joshua \"fox\"" . "\r\n";
echo "\njoshua" . " " . "fox";
echo "\r\njoshua" . PHP_EOL . "fox";